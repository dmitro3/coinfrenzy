// Blocked U.S. states for sweepstakes operations.
// Per docs/09 §8 — eleven blocked states currently.
// Update only with legal/compliance approval.
//
// This file is intentionally free of server-only imports so it can be
// pulled into client components (the signup form's state picker, etc).

export const BLOCKED_STATES: ReadonlySet<string> = new Set<string>([
  'CA',
  'CT',
  'ID',
  'LA',
  'MI',
  'MT',
  'NV',
  'NJ',
  'NY',
  'TN',
  'WA',
])

export function isBlockedState(state: string | null | undefined): boolean {
  if (!state) return false
  return BLOCKED_STATES.has(state.toUpperCase())
}

export type RestrictedFeatures = {
  canPlay: boolean
  canRedeem: boolean
  reason: string
}

// Reserved for the case where a state someday allows play but not redemption,
// or other partial restrictions. Empty today.
export const RESTRICTED_FEATURES_BY_STATE: Record<string, RestrictedFeatures> = {}

